/*
case(switch) statement
 */

let day = 45;

switch (day) {

    case 1 : 
    console.log("Saturday");
    break;

    case 2 : 
    console.log("Sunday");
    break;

    case 3 : 
    console.log("Monday");
    break;

    case 4 : 
    console.log("Tuesday");
    break;

    case 5 : 
    console.log("Wedensday");
    break;

    case 6 : 
    console.log("Thursday");
    break;

    case 7 : 
    console.log("Friday");
    break;

    default:
        console.log("Unkown day"); /*default value should be defined 
        to return an error otherwise nothing will output*/

}
